    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Obtener la contraseña ingresada
        String password = jTextField3.getText();

        // Validar la contraseña
        if (!validarPassword(password)) {
            JOptionPane.showMessageDialog(this, "Password no aceptado.\nDebe contener al menos 6 caracteres, una mayúscula, un número y un signo especial.", "Error", JOptionPane.ERROR_MESSAGE);
            return; // No permite continuar si la contraseña no es válida
        }

        // Si la contraseña es válida, crear el usuario
        Usuario u = new Usuario();
        u.setNombre(jTextField2.getText());
        u.setUsuario(jTextField1.getText());
        u.setPassword(jTextField3.getText());

        String rol = jComboBox1.getSelectedItem().toString();

        if (rol.equals("Administrador")) {
            u.setRol(1);
        } else {
            u.setRol(2);
        }

        ProyectoPichardo.getUsuarios().add(u);

        JOptionPane.showMessageDialog(this, "Usuario creado exitosamente.");
    }//GEN-LAST:event_jButton1ActionPerformed

