    private boolean validarPassword(String password) {
        // Expresión regular: mínimo 6 caracteres, al menos una mayúscula, un número y un signo especial
        String regex = "^(?=.*[A-Z])(?=.*[0-9])(?=.*[@#$%^&+=!]).{6,}$";

        return password.matches(regex);
    }

