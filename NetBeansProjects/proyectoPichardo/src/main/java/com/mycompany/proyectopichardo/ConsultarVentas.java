    private void pintarTabla() {
    DefaultTableModel modelo = new DefaultTableModel(
        new String[]{"Nombre", "NIT", "Direcci√≥n", "Total", "Total sin IVA", "Vendedor", "Fecha"},
        ProyectoPichardo.getVentas().size()
    );
    jTable1.setModel(modelo);

    for (int i = 0; i < ProyectoPichardo.getVentas().size(); i++) {
        Venta v = ProyectoPichardo.getVentas().get(i);
        double totalSinIVA = Math.round((v.total / 1.12) * 100.0) / 100.0;
        jTable1.setValueAt(v.nombre, i, 0);
        jTable1.setValueAt(v.nit, i, 1);
        jTable1.setValueAt(v.direccion, i, 2);
        jTable1.setValueAt(v.total, i, 3);
        jTable1.setValueAt(totalSinIVA, i, 4);
        jTable1.setValueAt(v.vendedor, i, 5);
        jTable1.setValueAt(v.fecha.getTime().toString(), i, 6);
    }
}

